function buildDailyForecast({ date = new Date(), latestRequest = null } = {}) {
  const current = new Date(date);
  const theme = THEMES[getDayIndex(current)];
  const personal = latestRequest ? buildPersonalAccent(latestRequest) : "Персональный акцент появится после расчета вашей натальной карты.";

  return [
    "🌙 Прогноз дня",
    "",
    `Сегодня энергия больше подходит для ${theme.title}.`,
    "",
    "Подходит:",
    formatList(theme.good),
    "",
    "Лучше избегать:",
    formatList(theme.avoid),
    "",
    "Ваш персональный акцент:",
    personal,
    "",
    "Совет дня:",
    theme.advice
  ].join("\n");
}

function buildPersonalAccent(request) {
  const moon = request.chart?.planets?.moon;
  const sun = request.chart?.planets?.sun;

  if (!moon || !sun) {
    return "Сегодня лучше прислушаться к своему состоянию и выбирать действия без лишнего давления.";
  }

  const houseTheme = {
    1: "самооценки, внешнего образа и личной инициативы",
    2: "денег, ресурсов и чувства устойчивости",
    3: "общения, обучения и коротких договоренностей",
    4: "дома, семьи и внутренней безопасности",
    5: "творчества, романтики и самовыражения",
    6: "работы, режима и заботы о теле",
    7: "отношений, партнерства и честных договоренностей",
    8: "глубоких эмоций, доверия и трансформации",
    9: "смысла, обучения и расширения горизонтов",
    10: "карьеры, целей и видимого результата",
    11: "друзей, команды и будущих планов",
    12: "отдыха, интуиции и скрытых переживаний"
  };

  return `Может сильнее проявляться тема ${houseTheme[moon.house] || "эмоций и внутреннего баланса"}. Солнце в ${sun.label ? "вашей карте" : "карте"} подсказывает: выбирайте действия, которые возвращают ощущение личной силы.`;
}

function getDayIndex(date) {
  const seed = date.getUTCFullYear() * 10000 + (date.getUTCMonth() + 1) * 100 + date.getUTCDate();
  return seed % THEMES.length;
}

function formatList(items) {
  return items.map((item) => `• ${item}`).join("\n");
}

module.exports = { buildDailyForecast };
